package org.xbib.datastructures.io;

public class Information {

    private byte separator;

    private int count;

    public void setSeparator(byte separator) {
        this.separator = separator;
    }

    public byte getSeparator() {
        return separator;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public int getCount() {
        return count;
    }
}
